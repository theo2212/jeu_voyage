function initCanvas() {
    const canvas = document.getElementById('drawing-canvas');
    if (!canvas) return;
    
    // Ajuster la taille rГѓЖ’Г‚В©elle du canvas
    const rect = canvas.getBoundingClientRect();
    canvas.width = rect.width * window.devicePixelRatio;
    canvas.height = rect.height * window.devicePixelRatio;
    
    drawingContext = canvas.getContext('2d');
    drawingContext.scale(window.devicePixelRatio, window.devicePixelRatio);
    drawingContext.lineCap = 'round';
    drawingContext.lineWidth = currentBrushSize;
    drawingContext.strokeStyle = isEraserActive ? '#ffffff' : currentBrushColor;
    if (isEraserActive) drawingContext.globalCompositeOperation = 'destination-out';

    const startDraw = (e) => {
        isDrawing = true;
        const pos = getMousePos(canvas, e);
        drawingContext.beginPath();
        drawingContext.moveTo(pos.x, pos.y);
    };

    const doDraw = (e) => {
        if (!isDrawing) return;
        const pos = getMousePos(canvas, e);
        drawingContext.lineTo(pos.x, pos.y);
        drawingContext.stroke();
        
        throttleCanvasSync();
    };

    const stopDraw = () => {
        isDrawing = false;
        syncCanvas();
    };

    canvas.addEventListener('mousedown', startDraw);
    canvas.addEventListener('mousemove', doDraw);
    canvas.addEventListener('mouseup', stopDraw);
    canvas.addEventListener('mouseleave', stopDraw);

    canvas.addEventListener('touchstart', (e) => { 
        const touch = e.touches[0];
        const mouseEvent = new MouseEvent('mousedown', {
            clientX: touch.clientX,
            clientY: touch.clientY
        });
        canvas.dispatchEvent(mouseEvent);
        e.preventDefault();
    }, { passive: false });

    canvas.addEventListener('touchmove', (e) => {
        const touch = e.touches[0];
        const mouseEvent = new MouseEvent('mousemove', {
            clientX: touch.clientX,
            clientY: touch.clientY
        });
        canvas.dispatchEvent(mouseEvent);
        e.preventDefault();
    }, { passive: false });

    canvas.addEventListener('touchend', (e) => {
        const mouseEvent = new MouseEvent('mouseup', {});
        canvas.dispatchEvent(mouseEvent);
        e.preventDefault();
    }, { passive: false });
}

function getMousePos(canvas, evt) {
    const rect = canvas.getBoundingClientRect();
    return {
        x: evt.clientX - rect.left,
        y: evt.clientY - rect.top
    };
}

function throttleCanvasSync() {
    const now = Date.now();
    if (now - lastCanvasUpdate > 250) { 
        syncCanvas();
        lastCanvasUpdate = now;
    }
}

