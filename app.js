function validateQuizPlayer(playerId, isCorrect, btn) {
    triggerVibe(30);
    btn.parentElement.innerHTML = isCorrect ? "<span style='color:#30d158;'>ValidГѓЖ’Г‚В© ГѓВўГ…вЂњГўв‚¬В¦</span>" : "<span style='color:#ff3b30;'>Faux ГѓВўГ‚ВќГ…вЂ™</span>";
    
    roomChannel.send({
        type: 'broadcast',
        event: 'quiz_verdict',
        payload: { playerId: playerId, isCorrect: isCorrect }
    });

    // Update local score if admin is the one validated (unlikely but possible)
    if (isCorrect) {
        const player = roomPlayers.find(p => p.id === playerId);
        if (player) player.score = (player.score || 0) + 1;
    }
}

