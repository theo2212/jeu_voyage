function nextQuizQuestion() {
    currentQuizIndex++;
    if (currentQuizIndex < 10) {
        quizAnswersReceived = [];
        roomChannel.send({
            type: 'broadcast',
            event: 'quiz_next',
            payload: { index: currentQuizIndex }
        });
        loadQuizQuestion();
    } else {
        roomChannel.send({ type: 'broadcast', event: 'quiz_end', payload: { finalPlayers: roomPlayers } });
        showQuizPodium();
    }
}
